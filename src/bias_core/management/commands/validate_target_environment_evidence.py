    def _load_json(self, path: Path, key: str) -> tuple[dict[str, Any] | None, dict[str, Any] | None]:
        if not path.exists():
            return None, self._result(key, path, False, missing=True, errors=[f"missing evidence file: {path.name}"])
        try:
            data = json.loads(path.read_text(encoding="utf-8"))
        except (OSError, json.JSONDecodeError) as exc:
            return None, self._result(key, path, False, errors=[f"invalid JSON evidence: {type(exc).__name__}: {exc}"])
        if not isinstance(data, dict):
            return None, self._result(key, path, False, errors=["JSON evidence root must be an object"])
        return data, None

