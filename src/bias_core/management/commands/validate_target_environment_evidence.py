    def _is_local_backup_path(self, raw_path: str) -> bool:
        value = raw_path.strip().replace("\\", "/").lower()
        if not value:
            return True
        if "://" in value:
            return value.startswith(("file://", "sqlite://"))
        return (
            value.startswith("/app/")
            or value.startswith("/tmp/")
            or value.startswith("/var/tmp/")
            or value.startswith("backups/")
            or "/tmp/" in value
            or "/.tmp-" in value
            or "/project/tmp/" in value
            or value[1:3] == ":/" and "/tmp/" in value
        )

