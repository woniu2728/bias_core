        if summary.get("ok") is not True:
            errors.append("summary.ok must be true")
        if summary.get("multi_node") is not True:
            errors.append("summary.multi_node must be true")
        minimums = {"web": 2 if require_multi_node else 1, "worker": 1, "scheduler": 1}
        for role, minimum in minimums.items():
            count = roles.get(role)
            if not isinstance(count, int) or count < minimum:
                errors.append(f"roles.{role} must be at least {minimum}")
        image = str(artifacts.get("image") or "").strip()
        version = str(artifacts.get("version") or "").strip()
        if not image:
            errors.append("artifacts.image is required")
        elif image in {"local-production-smoke", "latest"} or self._has_placeholder(image):
            errors.append("artifacts.image must identify a target release image")
        if not version:
            errors.append("artifacts.version is required")
        elif self._has_placeholder(version):
            errors.append("artifacts.version must not contain placeholders")
        database = str(shared_services.get("database") or "").strip()
        redis = str(shared_services.get("redis") or "").strip()
        load_balancer = str(shared_services.get("load_balancer") or "").strip()
