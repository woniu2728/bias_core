    def _dependency_graph_errors(self, command_by_key: dict[str, dict[str, Any]]) -> list[str]:
        errors = []
        graph: dict[str, list[str]] = {}
        for key, command in command_by_key.items():
            dependencies = self._string_list(command.get("requires_completed_commands"))
            graph[key] = dependencies
            for dependency in dependencies:
                if dependency not in command_by_key:
                    errors.append(f"commands.{key}.requires_completed_commands references unknown command {dependency}")
                if dependency == key:
                    errors.append(f"commands.{key}.requires_completed_commands must not reference itself")
        visited: set[str] = set()
        visiting: list[str] = []

        def visit(key: str) -> None:
            if key in visited:
                return
            if key in visiting:
                cycle = visiting[visiting.index(key):] + [key]
                errors.append(f"commands.requires_completed_commands contains cycle: {' -> '.join(cycle)}")
                return
            visiting.append(key)
            for dependency in graph.get(key, []):
                if dependency in command_by_key:
                    visit(dependency)
            visiting.pop()
            visited.add(key)

        for key in graph:
            visit(key)
        return errors

    def _dependency_execution_waves(self, commands: list[dict[str, Any]]) -> list[dict[str, Any]]:
        command_by_key = {str(command.get("key") or ""): command for command in commands}
        depth_cache: dict[str, int] = {}

        def depth_for(key: str, stack: tuple[str, ...] = ()) -> int:
            if key in depth_cache:
                return depth_cache[key]
            if key in stack:
                return 0
            command = command_by_key.get(key)
            if not command:
                return 0
            dependencies = [
                dependency
                for dependency in self._string_list(command.get("requires_completed_commands"))
                if dependency in command_by_key
            ]
            if not dependencies:
                depth_cache[key] = 0
                return 0
            depth_cache[key] = 1 + max(depth_for(dependency, stack + (key,)) for dependency in dependencies)
            return depth_cache[key]

        grouped: dict[int, list[dict[str, Any]]] = {}
        for command in commands:
            if not self._string_list(command.get("requires_completed_commands")):
                continue
            grouped.setdefault(depth_for(str(command.get("key") or "")), []).append(command)
        waves = []
        for index, depth in enumerate(sorted(grouped), start=1):
            wave_commands = grouped[depth]
            waves.append({
                "wave": index,
                "dependency_depth": depth,
                "command_count": len(wave_commands),
                "command_keys": [str(item.get("key") or "") for item in wave_commands],
                "requires_completed_commands": self._unique_dependencies(wave_commands),
                "commands": [self._dependency_wave_command_metadata(item) for item in wave_commands],
            })
        return waves

