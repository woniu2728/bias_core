    def _command_contains_fragment(self, command: str, fragment: str) -> bool:
        normalized_command = " ".join(command.split())
        normalized_fragment = " ".join(fragment.split())
        if not normalized_command or not normalized_fragment:
            return False
        if " " not in normalized_fragment:
            return normalized_fragment in normalized_command
        pattern = r"(?<!\S)" + r"\s+".join(re.escape(part) for part in normalized_fragment.split()) + r"(?!\S)"
        return re.search(pattern, normalized_command) is not None

