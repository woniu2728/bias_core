    def _check_queue_worker(self, path: Path, *, key: str) -> dict[str, Any]:
        data, error = self._load_json(path, key)
        if error:
            return error
        summary = self._summary(data or {})
        broker_url = str((data or {}).get("broker_url") or "")
        result_backend = str((data or {}).get("result_backend") or "")
        token = str((data or {}).get("token") or "")
        worker_status = (data or {}).get("worker_status")
        task_result = (data or {}).get("task_result")
        if not isinstance(worker_status, dict):
            worker_status = {}
        if not isinstance(task_result, dict):
            task_result = {}
        errors = []
        if summary.get("ok") is not True:
            errors.append("summary.ok must be true")
        if worker_status.get("available") is not True:
            errors.append("worker_status.available must be true")
        worker_count = worker_status.get("worker_count")
        if not isinstance(worker_count, int) or worker_count < 1:
            errors.append("worker_status.worker_count must be at least 1")
        if task_result.get("ok") is not True:
            errors.append("task_result.ok must be true")
        if not token or task_result.get("token") != token:
            errors.append("task_result.token must match the dispatched token")
