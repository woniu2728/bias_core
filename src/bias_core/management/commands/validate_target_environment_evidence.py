from django.core.management.base import CommandParser


class Command(BaseCommand):
    help = "Validate archived JSON evidence for the target-environment P2 gate."
    requires_system_checks = []

    def add_arguments(self, parser: CommandParser) -> None:
        parser.add_argument(
            "--report-dir",
            required=True,
            help="Directory containing archived target-environment JSON evidence.",
        )
        parser.add_argument(
            "--p0-report-dir",
            help="Directory containing target-environment P0 anonymous-read capacity evidence.",
        )
        parser.add_argument(
            "--p1-report-dir",
            help="Directory containing target-environment P1 authenticated/write capacity evidence.",
        )
        parser.add_argument(
            "--require-multi-node",
            action="store_true",
            help="Require multi-node topology evidence for the target environment.",
        )
        parser.add_argument(
            "--plan-file",
            help="Optional target-environment evidence plan JSON generated by plan_target_environment_evidence.",
        )
        parser.add_argument(
            "--write-remediation-checklist",
            default="",
            help="Write a Markdown follow-up checklist from failed remediation actions.",
        )
        parser.add_argument("--format", choices=("text", "json"), default="text")

    def handle(self, *args, **options):
        report_dir = self._resolve_path(options["report_dir"])
        p0_report_dir = self._resolve_optional_path(options.get("p0_report_dir"))
        p1_report_dir = self._resolve_optional_path(options.get("p1_report_dir"))
        plan_file = self._resolve_optional_path(options.get("plan_file"))
        checks = self._build_checks(
            report_dir,
            p0_report_dir=p0_report_dir,
            p1_report_dir=p1_report_dir,
            require_multi_node=bool(options["require_multi_node"]),
            plan_file=plan_file,
        )
        failed = [check for check in checks if not check["ok"]]
        plan_commands_by_key = self._load_plan_commands(plan_file) if plan_file else {}
        remediation = self._build_remediation(failed, plan_commands_by_key=plan_commands_by_key)
        payload = {
            "report_dir": str(report_dir),
            "p0_report_dir": str(p0_report_dir) if p0_report_dir else "",
            "p1_report_dir": str(p1_report_dir) if p1_report_dir else "",
            "plan_file": str(plan_file) if plan_file else "",
            "checks": checks,
            "remediation": remediation,
            "summary": {
                "ok": not failed,
                "check_count": len(checks),
                "failed_count": len(failed),
                "missing_count": sum(1 for check in checks if check.get("missing")),
                "remediation_action_count": len(remediation["actions"]),
                "target_environment_ready": not failed,
                "require_multi_node": bool(options["require_multi_node"]),
            },
        }
        checklist_path = self._resolve_optional_path(options.get("write_remediation_checklist"))
        if checklist_path is not None:
            self._write_remediation_checklist(checklist_path, payload)
            payload["remediation"]["checklist_path"] = str(checklist_path)

        if options["format"] == "json":
            self.stdout.write(json.dumps(payload, ensure_ascii=False, indent=2))
        else:
            self._write_text(payload)

        if failed:
            raise CommandError("Target-environment evidence gate failed")

    def _build_remediation(
        self,
        failed: list[dict[str, Any]],
        *,
        plan_commands_by_key: dict[str, dict[str, Any]] | None = None,
    ) -> dict[str, Any]:
        missing = [check for check in failed if check.get("missing")]
        plan_commands_by_key = plan_commands_by_key or {}
        priority = [
            "target_evidence_plan",
            "target_archive_integrity",
            "strict_health",
            "https_http_smoke",
            "external_websocket",
            "queue_worker",
            "backup",
            "backup_verification",
            "upgrade_executed",
            "post_upgrade_strict_health",
            "post_upgrade_http_smoke",
            "post_upgrade_queue_worker",
            "rollback_plan",
            "restore_rehearsal",
            "restore_dry_run",
            "live_restore",
            "runtime_integrations",
            "multi_node_topology",
            "p0_capacity_suite",
            "p1_capacity_suite",
        ]
        failed_by_key = {str(check.get("key") or ""): check for check in failed}
        actions = []
        for key in priority:
            check = failed_by_key.get(key)
            if not check:
                continue
            actions.append(self._remediation_action(key, check, plan_commands_by_key))
        for key, check in failed_by_key.items():
            if key in priority:
                continue
            actions.append(self._remediation_action(key, check, plan_commands_by_key))
        planned_commands = self._unique_remediation_planned_commands(actions)
        command_groups = self._remediation_command_groups(planned_commands)
        execution_queues = self._remediation_execution_queues(planned_commands)
        dependency_execution_waves = self._dependency_execution_waves(planned_commands)
        return {
            "blocked": bool(failed),
            "failed_keys": [str(check.get("key") or "") for check in failed],
            "missing_files": [str(check.get("file") or "") for check in missing],
            "actions": actions,
            "command_groups": command_groups,
            "execution_sequence": self._remediation_execution_sequence(command_groups),
            "execution_queues": execution_queues,
            "execution_queue_counts": self._queue_counts(execution_queues),
            "dependency_execution_waves": dependency_execution_waves,
            "dependency_execution_wave_count": len(dependency_execution_waves),
        }

    def _unique_remediation_planned_commands(self, actions: list[dict[str, Any]]) -> list[dict[str, Any]]:
        commands = []
        seen = set()
        for action in actions:
            action_key = str(action.get("key") or "")
            for command in action.get("planned_commands", []):
                if not isinstance(command, dict):
                    continue
                key = str(command.get("key") or "")
                if not key or key in seen:
                    continue
                seen.add(key)
                commands.append({**command, "action_key": action_key})
        return commands

    def _remediation_command_groups(self, planned_commands: list[dict[str, Any]]) -> dict[str, dict[str, Any]]:
        grouped: dict[str, dict[str, Any]] = {}
        for command in planned_commands:
            if not isinstance(command, dict):
                continue
            action_key = str(command.get("action_key") or "")
            group = str(command.get("execution_group") or "unknown")
            group_payload = grouped.setdefault(group, {
                    "command_count": 0,
                    "action_keys": [],
                    "command_keys": [],
                    "commands": [],
                    "archive_commands": [],
                    "output_files": [],
                    "stderr_files": [],
                    "manual_approval_required": False,
                    "destructive": False,
                    "safe_to_run_unattended": group == "safe_unattended",
                    "safe_archive_ready": group == "safe_unattended",
                    "requires_substitution": False,
                    "target_value_required": False,
                    "dependency_blocked": False,
                    "requires_completed_commands": [],
            })
            group_payload["command_count"] += 1
            if action_key and action_key not in group_payload["action_keys"]:
                group_payload["action_keys"].append(action_key)
            command_key = str(command.get("key") or "")
            if command_key:
                group_payload["command_keys"].append(command_key)
            raw_command = str(command.get("command") or "")
            if raw_command:
                group_payload["commands"].append(raw_command)
            archive_command = str(command.get("archive_command") or "")
            if archive_command:
                group_payload["archive_commands"].append(archive_command)
            output_file = str(command.get("output_file") or "")
            if output_file:
                group_payload["output_files"].append(output_file)
            stderr_file = str(command.get("stderr_file") or "")
            if stderr_file:
                group_payload["stderr_files"].append(stderr_file)
            if command.get("manual_approval_required"):
                group_payload["manual_approval_required"] = True
            if command.get("destructive"):
                group_payload["destructive"] = True
            if not command.get("safe_to_run_unattended"):
                group_payload["safe_to_run_unattended"] = False
            if not command.get("safe_archive_ready"):
                group_payload["safe_archive_ready"] = False
            if command.get("requires_substitution"):
                group_payload["requires_substitution"] = True
            if command.get("target_value_errors"):
                group_payload["target_value_required"] = True
            for dependency in self._string_list(command.get("requires_completed_commands")):
                group_payload["dependency_blocked"] = True
                if dependency not in group_payload["requires_completed_commands"]:
                    group_payload["requires_completed_commands"].append(dependency)
        return dict(sorted(grouped.items()))

    def _remediation_execution_sequence(self, command_groups: dict[str, dict[str, Any]]) -> list[dict[str, Any]]:
        order = [
            ("safe_unattended", "run safe unattended archive commands"),
            ("requires_substitution", "replace substitution tokens before running"),
            ("target_value_required", "fix target value errors before running"),
            ("maintenance_approval", "run only in an approved maintenance window"),
            ("destructive_approval", "run only after approval, durable backup, and rollback staffing are confirmed"),
            ("final_validation", "run only after all evidence-producing commands have been archived"),
        ]
        sequence = []
        step = 1
        seen = set()
        for group, policy in order:
            data = command_groups.get(group)
            if not data:
                continue
            sequence.append(self._remediation_sequence_item(step, group, data, policy))
            seen.add(group)
            step += 1
        for group in sorted(set(command_groups) - seen):
            sequence.append(self._remediation_sequence_item(
                step,
                group,
                command_groups[group],
                "manual review required before running",
            ))
            step += 1
        return sequence

    def _remediation_sequence_item(self, step: int, group: str, data: dict[str, Any], policy: str) -> dict[str, Any]:
        return {
            "step": step,
            "groups": [group],
            "action_keys": data["action_keys"],
            "command_keys": data["command_keys"],
            "command_count": data["command_count"],
            "safe_to_run_unattended": data["safe_to_run_unattended"],
            "safe_archive_ready": data["safe_archive_ready"],
            "manual_approval_required": data["manual_approval_required"],
            "destructive": data["destructive"],
            "requires_substitution": data["requires_substitution"],
            "target_value_required": data["target_value_required"],
            "dependency_blocked": data["dependency_blocked"],
            "requires_completed_commands": data["requires_completed_commands"],
            "policy": policy,
        }

    def _remediation_execution_queues(self, planned_commands: list[dict[str, Any]]) -> dict[str, dict[str, Any]]:
        queues = {
            "safe_unattended": {
                "policy": "eligible for unattended archive execution after target values are reviewed",
                "commands": [],
            },
            "requires_substitution": {
                "policy": "replace substitution tokens before running",
                "commands": [],
            },
            "target_value_required": {
                "policy": "fix target value errors before running",
                "commands": [],
            },
            "dependency_blocked": {
                "policy": "run only after requires_completed_commands have been archived",
                "commands": [],
            },
            "maintenance_approval": {
                "policy": "run only in an approved maintenance window",
                "commands": [],
            },
            "destructive_approval": {
                "policy": "run only after destructive approval and durable backup confirmation",
                "commands": [],
            },
            "final_validation": {
                "policy": "run only after all evidence-producing commands have been archived",
                "commands": [],
            },
        }
        for command in planned_commands:
            if not isinstance(command, dict):
                continue
            action_key = str(command.get("action_key") or "")
            command_ref = self._remediation_queue_command(action_key, command)
            if command.get("safe_to_run_unattended"):
                queues["safe_unattended"]["commands"].append(command_ref)
            if command.get("requires_substitution"):
                queues["requires_substitution"]["commands"].append(command_ref)
            if command.get("target_value_errors"):
                queues["target_value_required"]["commands"].append(command_ref)
            if command.get("requires_completed_commands"):
                queues["dependency_blocked"]["commands"].append(command_ref)
            if command.get("execution_group") == "maintenance_approval" or (
                command.get("manual_approval_required") and not command.get("destructive")
            ):
                queues["maintenance_approval"]["commands"].append(command_ref)
            if command.get("destructive"):
                queues["destructive_approval"]["commands"].append(command_ref)
            if command.get("execution_group") == "final_validation":
                queues["final_validation"]["commands"].append(command_ref)
        return {key: self._remediation_queue_payload(value) for key, value in queues.items()}

    def _remediation_queue_command(self, action_key: str, command: dict[str, Any]) -> dict[str, Any]:
        return {
            "action_key": action_key,
            "key": str(command.get("key") or ""),
            "execution_group": str(command.get("execution_group") or ""),
            "command": str(command.get("command") or ""),
            "output_file": str(command.get("output_file") or ""),
            "stderr_file": str(command.get("stderr_file") or ""),
            "archive_command": str(command.get("archive_command") or ""),
            "safe_to_run_unattended": command.get("safe_to_run_unattended") is True,
            "safe_archive_ready": command.get("safe_archive_ready") is True,
            "manual_approval_required": command.get("manual_approval_required") is True,
            "destructive": command.get("destructive") is True,
            "requires_substitution": command.get("requires_substitution") is True,
            "substitution_tokens": self._string_list(command.get("substitution_tokens")),
            "target_value_errors": self._string_list(command.get("target_value_errors")),
            "requires_completed_commands": self._string_list(command.get("requires_completed_commands")),
            "base_url": str(command.get("base_url") or ""),
            "backup_dir": str(command.get("backup_dir") or ""),
            "redis_broker_url": str(command.get("redis_broker_url") or ""),
            "redis_result_backend": str(command.get("redis_result_backend") or ""),
            "topology": command.get("topology") if isinstance(command.get("topology"), dict) else {},
            "capacity_profile": command.get("capacity_profile") if isinstance(command.get("capacity_profile"), dict) else {},
            "websocket_profile": command.get("websocket_profile") if isinstance(command.get("websocket_profile"), dict) else {},
            "runtime_integration_profile": command.get("runtime_integration_profile") if isinstance(command.get("runtime_integration_profile"), dict) else {},
            "validation_profile": command.get("validation_profile") if isinstance(command.get("validation_profile"), dict) else {},
        }

    def _remediation_queue_payload(self, queue: dict[str, Any]) -> dict[str, Any]:
        commands = [item for item in queue["commands"] if isinstance(item, dict)]
        return {
            "policy": str(queue.get("policy") or ""),
            "command_count": len(commands),
            "action_keys": self._unique_strings(item.get("action_key") for item in commands),
            "command_keys": [str(item.get("key") or "") for item in commands if item.get("key")],
            "archive_commands": [str(item.get("archive_command") or "") for item in commands if item.get("archive_command")],
            "commands": commands,
        }

    def _queue_counts(self, execution_queues: dict[str, dict[str, Any]]) -> dict[str, int]:
        return {
            key: int(value.get("command_count") or 0)
            for key, value in execution_queues.items()
            if isinstance(value, dict)
        }

    def _unique_strings(self, values) -> list[str]:
        result = []
        for value in values:
            text = str(value or "")
            if text and text not in result:
                result.append(text)
        return result

    def _duplicate_strings(self, values) -> list[str]:
        seen = set()
        duplicates = []
        for value in values:
            text = str(value or "")
            if not text:
                continue
            if text in seen and text not in duplicates:
                duplicates.append(text)
            seen.add(text)
        return duplicates

    def _missing_key_count(self, items: list[dict[str, Any]]) -> int:
        return sum(1 for item in items if not str(item.get("key") or "").strip())

    def _object_list_errors(self, field_path: str, raw_value: Any, normalized_value: list[Any]) -> list[str]:
        if not isinstance(raw_value, list):
            return [f"{field_path} must be a list"]
        if len(raw_value) != len(normalized_value):
            return [f"{field_path} items must be objects"]
        return []

    def _bool_field_errors(
        self,
        collection_name: str,
        items: list[dict[str, Any]],
        field_names: tuple[str, ...],
    ) -> list[str]:
        errors = []
        for item in items:
            key = str(item.get("key") or "")
            for field_name in field_names:
                if not isinstance(item.get(field_name), bool):
                    errors.append(f"{collection_name}.{key}.{field_name} must be a boolean")
        return errors

    def _string_list_item_errors(self, field_path: str, value: list[Any]) -> list[str]:
        if all(isinstance(item, str) and item.strip() for item in value):
            return []
        return [f"{field_path} items must be non-empty strings"]

    def _list_field_item_errors(
        self,
        collection_name: str,
        items: list[dict[str, Any]],
        field_names: tuple[str, ...],
    ) -> list[str]:
        errors = []
        for item in items:
            key = str(item.get("key") or "")
            for field_name in field_names:
                value = item.get(field_name)
                if isinstance(value, list):
                    errors.extend(self._string_list_item_errors(f"{collection_name}.{key}.{field_name}", value))
        return errors

    def _command_group_structure_errors(self, group: str, payload: dict[str, Any]) -> list[str]:
        errors = []
        for field_name in (
            "command_keys",
            "commands",
            "output_files",
            "stderr_files",
            "archive_commands",
            "requires_completed_commands",
        ):
            value = payload.get(field_name)
            if not isinstance(value, list):
                errors.append(f"command_groups.{group}.{field_name} must be a list")
            else:
                errors.extend(self._string_list_item_errors(f"command_groups.{group}.{field_name}", value))
        for field_name in (
            "manual_approval_required",
            "destructive",
            "safe_to_run_unattended",
            "safe_archive_ready",
            "requires_substitution",
            "target_value_required",
            "dependency_blocked",
        ):
            if not isinstance(payload.get(field_name), bool):
                errors.append(f"command_groups.{group}.{field_name} must be a boolean")
        return errors

    def _remediation_action(
        self,
        key: str,
        check: dict[str, Any],
        plan_commands_by_key: dict[str, dict[str, Any]],
    ) -> dict[str, Any]:
        action = {
            "key": key,
            "file": check.get("file", ""),
            "missing": bool(check.get("missing")),
            "error_count": len(check.get("errors", [])),
            "errors": self._string_list(check.get("errors")),
            "hint": self._remediation_hint(key),
        }
        details = check.get("details")
        if isinstance(details, dict) and details:
            action["details"] = details
        plan_commands = self._remediation_plan_commands(key, check, plan_commands_by_key)
        if plan_commands:
            action["planned_commands"] = plan_commands
        return action

    def _remediation_plan_commands(
        self,
        key: str,
        check: dict[str, Any],
        plan_commands_by_key: dict[str, dict[str, Any]],
    ) -> list[dict[str, Any]]:
        if key in {"target_archive_integrity", "target_plan_evidence_alignment", "target_dependency_evidence"}:
            details = check.get("details") if isinstance(check.get("details"), dict) else {}
            plan_keys = self._unique_strings(
                list(details.get("missing_output_keys") or [])
                + list(details.get("missing_stderr_keys") or [])
                + list(details.get("non_empty_stderr_keys") or [])
                + list(details.get("mismatched_command_keys") or [])
                + list(details.get("failed_dependency_keys") or [])
                + list(details.get("blocked_command_keys") or [])
            )
            return [
                self._plan_command_metadata(plan_commands_by_key[plan_key])
                for plan_key in plan_keys
                if plan_key in plan_commands_by_key
            ]
        plan_keys_by_check = {
            "p0_capacity_suite": ["p0_forum_main"],
            "p1_capacity_suite": [
                "p1_forum_main_auth",
                "p1_forum_write",
                "p1_forum_write_mixed",
                "p1_forum_upload",
                "p1_forum_moderation",
            ],
            "target_evidence_plan": ["validate_target_environment_evidence"],
        }
        plan_keys = plan_keys_by_check.get(key, [key])
        commands = []
        for plan_key in plan_keys:
            command = plan_commands_by_key.get(plan_key)
            if not command:
                continue
            commands.append(self._plan_command_metadata(command))
        return commands

    def _plan_command_metadata(self, command: dict[str, Any]) -> dict[str, Any]:
        return {
            "key": str(command.get("key") or ""),
            "phase": str(command.get("phase") or ""),
            "execution_group": str(command.get("execution_group") or ""),
            "manual_approval_required": bool(command.get("manual_approval_required")),
            "destructive": bool(command.get("destructive")),
            "safe_to_run_unattended": bool(command.get("safe_to_run_unattended")),
            "safe_archive_ready": command.get("safe_archive_ready") is True,
            "requires_substitution": bool(command.get("requires_substitution")),
            "substitution_tokens": self._string_list(command.get("substitution_tokens")),
            "target_value_errors": self._string_list(command.get("target_value_errors")),
            "requires_completed_commands": self._string_list(command.get("requires_completed_commands")),
            "base_url": str(command.get("base_url") or ""),
            "backup_dir": str(command.get("backup_dir") or ""),
            "redis_broker_url": str(command.get("redis_broker_url") or ""),
            "redis_result_backend": str(command.get("redis_result_backend") or ""),
            "topology": command.get("topology") if isinstance(command.get("topology"), dict) else {},
            "capacity_profile": command.get("capacity_profile") if isinstance(command.get("capacity_profile"), dict) else {},
            "websocket_profile": command.get("websocket_profile") if isinstance(command.get("websocket_profile"), dict) else {},
            "runtime_integration_profile": command.get("runtime_integration_profile") if isinstance(command.get("runtime_integration_profile"), dict) else {},
            "validation_profile": command.get("validation_profile") if isinstance(command.get("validation_profile"), dict) else {},
            "command": str(command.get("command") or ""),
            "output_file": str(command.get("output_file") or ""),
            "stderr_file": str(command.get("stderr_file") or ""),
            "archive_command": str(command.get("archive_command") or ""),
        }

    def _string_list(self, value: Any) -> list[str]:
        if not isinstance(value, list):
            return []
        return [str(item) for item in value]

    def _dict_list(self, value: Any) -> list[dict[str, Any]]:
        if not isinstance(value, list):
            return []
        return [item for item in value if isinstance(item, dict)]

